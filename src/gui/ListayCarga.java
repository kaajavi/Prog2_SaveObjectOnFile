    private void clean() {
        Curso.load();
        this.txtCelu.setText("");
        this.txtName.setText("");
        this.txtDni.setText("");
        this.txtLegajo.setText("");
        this.txtConsola.setText("");
        load();

    }

    private void load() {

        DefaultListModel model = new DefaultListModel();
        for (Alumno alumno : Curso.getAlumnos()) {
            model.addElement(alumno);
        }

        jListAlumnos.setModel(model);

    }
