    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        String errores = "";
        int legajo=0;
        int dni=0;
        String nombre;
        String celu;
        
        try {
            legajo = Integer.parseInt(this.txtLegajo.getText());
        } catch (Exception ex) {
            errores += "Legajo debe ser un número.\n";
        }
        
        try {
            dni = Integer.parseInt(this.txtDni.getText());
        } catch (Exception ex) {
            errores += "Dni debe ser un número\n";
        }
        
        nombre = this.txtName.getText();
        if (nombre.length()==0){
            errores += "Debe ingresar un nombre\n";
        }
        
        celu = this.txtCelu.getText();
        
        if (errores.length()>0){
            txtConsola.setText(errores);
            return;
        }else{
            Alumno aux = new Alumno(legajo, dni, nombre, celu);
            Curso.addAlumno(aux);
            clean();
        }
    }//GEN-LAST:event_btnOkActionPerformed

